let friend1 = "Harry" // Friend 1 name
let friend2 = "Rohan" // Friend 2 name

console.log("My name is " + friend1 + " and my friend name is " + friend2) // String concatenation

// Template Literal
console.log(`My name is ${friend1} and my friend name is ${friend2}`) // Template literal

// Escape Sequence
console.log("His pet name is \"ROHU\"") // Escape double quotes

let myName = "Prabhat" // My name

console.log(myName.toUpperCase()) // Uppercase
console.log(myName.toLocaleLowerCase) // Lowercase (missing parentheses)
console.log(myName.length) // Length of string
console.log(myName.slice(2,6)) // Slice from 2 to 6
console.log(myName.slice(2)) // Slice from 2 to end
console.log(myName.replace("P","S")) // Replace P with S
console.log(myName.concat(friend1, friend2, "Aishwarya")) // Concatenate strings

let yourName = "  Sahil  " // Another name
console.log(yourName.trim()) // Trim spaces
console.log(yourName.endsWith("t")) // Ends with 't'
console.log(yourName.startsWith("P")) // Starts with 'P'
