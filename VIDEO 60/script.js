let friend1 = "Harry" // Friend 1
let friend2 = "Rohan" // Friend 2

console.log("My name is " + friend1 + " and my friend name is " + friend2) // String concat

// Template Literal
console.log(`My name is ${friend1} and my friend name is ${friend2}`) // Template literal

// Escape Sequence
console.log("His pet name is \"ROHU\"") // Escape quotes

let myName = "Prabhat" // My name

console.log(myName.toUpperCase()) // Uppercase
console.log(myName.toLocaleLowerCase()) // Lowercase
console.log(myName.length) // String length
console.log(myName.slice(2,6)) // Slice 2-6
console.log(myName.slice(2)) // Slice from 2
console.log(myName.replace("P","S")) // Replace char
console.log(myName.concat(friend1, friend2, "Aishwarya")) // Concatenate

